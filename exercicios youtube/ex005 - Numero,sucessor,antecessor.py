# Faça um programa que leia um numero inteiro e mostre na tela o seu sucessor e antecessor.
numero = int(input("Digite um numero : "))

antecessor = numero - 1
sucessor = numero + 1


print(f'O numero digitado foi {numero}\nSeu antecessor é {antecessor}\nSeu sucessor é {sucessor}')

